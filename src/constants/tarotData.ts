export interface TimelineAdvice {
    past: string;
    present: string;
    future: string;
}

export interface CelticAdvice {
    core: string;        // 1번(현재), 5번(의식/목표) -> 현재의 중심 에너지
    obstacle: string;    // 2번(장애물) -> 당신을 가로막는 것
    foundation: string;  // 3번(무의식), 4번(과거) -> 문제의 뿌리와 기반
    nearFuture: string;  // 6번(가까운 미래) -> 곧 다가올 변화
    influence: string;   // 7번(태도), 8번(주변 상황) -> 내면과 외주의 상호작용
    destiny: string;     // 9번(희망/공포), 10번(최종 결과) -> 운명의 결론
}

